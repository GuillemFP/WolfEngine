ModuleEditor::~ModuleEditor()
{
	RELEASE(console);
}

bool ModuleEditor::Init()
{
	ImGui_ImplSdlGL3_Init(App->window->GetWindow());
	ref = new ImGuiStyle();
	
	panels.push_back(menu = new PanelMenuBar());
	panels.push_back(interfaces = new PanelInterface());
	return true;
}

update_status ModuleEditor::PreUpdate(float dt)
{
	ImGui_ImplSdlGL3_NewFrame(App->window->GetWindow());

	return UPDATE_CONTINUE;
}

update_status ModuleEditor::Update(float dt)
{
	for (std::vector<Panel*>::iterator it = panels.begin(); it != panels.end(); ++it)
		if ((*it)->active)
			(*it)->Draw();

	if (console->active)
		console->Draw();

	return UPDATE_CONTINUE;
}

bool ModuleEditor::CleanUp()
{
	RELEASE(ref);

	for (std::vector<Panel*>::iterator it = panels.begin(); it != panels.end(); ++it)
		RELEASE(*it);

	ImGui_ImplSdlGL3_Shutdown();
	return true;
}

void ModuleEditor::HandleInput(SDL_Event* event)
{
	ImGui_ImplSdlGL3_ProcessEvent(event);
}

void ModuleEditor::Draw()
{
	ImGui::Render();
}

void ModuleEditor::AddLog(const char* fmt)
{
	if (console != nullptr)
		console->AddLog(fmt);
}
